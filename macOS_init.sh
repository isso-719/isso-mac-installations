#!/bin/zsh

########################################################################
# Install Xcode Command Line Tools
########################################################################
sh XcodeCommandLineTools/install_Xcode_command_line_tools.sh

########################################################################
# Install Rosetta 2
########################################################################
sh Rosetta2/install_Rosetta2.sh

########################################################################
# Generate ~/.zshrc
########################################################################
cat <<EOF > ~/.zshrc
#
# ~/.zshrc
#
EOF

########################################################################
# Install Homebrew
########################################################################
/bin/bash -c "$(curl -fsSL https://raw.githubusercontent.com/Homebrew/install/HEAD/install.sh)"
